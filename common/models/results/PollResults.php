<?php

namespace common\models\results;

use common\components\ActiveRecord;
use common\models\Polls;
use common\models\Users;
use Yii;

/**
 * This is the model class for table "results.poll_results".
 *
 * @property int $id
 * @property int $poll_id
 * @property int $user_id
 * @property string $result
 * @property string $info
 * @property string $ts
 */
class PollResults extends ActiveRecord
{
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return 'results.poll_results';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return array_merge(parent::rules(), [
            [['info'], 'safe'],
            [['poll_id', 'result'], 'required'],
            [['poll_id'], 'integer'],
            [['result'], 'string', 'max' => 200],
        ]);
    }

    public function getUser()
    {
        return $this->hasOne(Users::className(), ["id" => "user_id"]);
    }

    public function getPoll()
    {
        return $this->hasOne(Polls::className(), ['id' => 'poll_id']);
    }

    public function afterSave($insert, $changedAttributes)
    {

        if ($insert) {
            $poll = $this->poll;
            $results = $poll->results;
            if (!$results) {
                $results = [];
            }
            $results[$this->result] = $results[$this->result] ? $results[$this->result]+1 : 1;
            $poll->results = $results;
            $poll->save();
        }

        parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
    }

}
